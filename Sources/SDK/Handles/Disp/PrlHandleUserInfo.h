#ifndef __PARALLELS_HANDLE_USER_INFO_H__
#define __PARALLELS_HANDLE_USER_INFO_H__


#include "PrlHandleBase.h"
#include <prlxmlmodel/UserInformation/ParallelsUserInformation.h>


/**
 * Handle object represents user information
 */
class PrlHandleUserInfo : public PrlHandleBase
{
public:

	/**
	 * Class default constructor.
	 */
	PrlHandleUserInfo(const QString& sUserInfo);

	/**
	 * Initialize object from XML string
	 */
	PRL_RESULT fromString(PRL_CONST_STR sXml);

	/**
	 * Returns object as XML string
	 */
	QString toString();

	/**
	 * Returns user name
	 * May be empty
	 * @param [out] buffer for storing result
	 * @param [out] buffer size (on in - how much symbols can be placed into buffer;
	 * on out - how much were placed actually)
	 * @return PRL_RESULT. Possible values:
	 * * PRL_ERR_INVALID_ARG - incorrect passed buffer size (NULL size)
	 * * PRL_ERR_BUFFER_OVERRUN - not enough buffer to completely storing result (in this case buffer will be contain
	 * * non fully string value)
	 * * PRL_ERR_SUCCESS - operation completed successfully
	 */
	PRL_RESULT GetName(PRL_STR sName, PRL_UINT32_PTR pnNameBufLength);

	/**
	 * Returns user universal unique identificator (uuid)
	 * May be empty
	 * @param [out] buffer for storing result
	 * @param [out] buffer size (on in - how much symbols can be placed into buffer;
	 * on out - how much were placed actually)
	 * @return PRL_RESULT. Possible values:
	 * * PRL_ERR_INVALID_ARG - incorrect passed buffer size (NULL size)
	 * * PRL_ERR_BUFFER_OVERRUN - not enough buffer to completely storing result (in this case buffer will be contain
	 * * non fully string value)
	 * * PRL_ERR_SUCCESS - operation completed successfully
	 */
	PRL_RESULT GetUuid(PRL_STR sUuid, PRL_UINT32_PTR pnUuidBufLength);

	/**
	 * Returns user active session count
	 * @param [out] buffer for storing result
	 * @return PRL_RESULT. Possible values:
	 * PRL_ERR_SUCCESS - operation completed successfully
	 */
	PRL_RESULT GetSessionCount(PRL_UINT32_PTR pnSessionCount);

	/**
	 * Returns path of default folder for users VM
	 * May be empty
	 * @param [out] buffer for storing result
	 * @param [out] buffer size (on in - how much symbols can be placed into buffer;
	 * on out - how much were placed actually)
	 * @return PRL_RESULT. Possible values:
	 * * PRL_ERR_INVALID_ARG - incorrect passed buffer size (NULL size)
	 * * PRL_ERR_BUFFER_OVERRUN - not enough buffer to completely storing result (in this case buffer will be contain
	 * * non fully string value)
	 * * PRL_ERR_SUCCESS - operation completed successfully
	 */
	PRL_RESULT GetDefaultVmFolder(PRL_STR sDefaultVmFolder, PRL_UINT32_PTR pnDefaultVmFolderBufLength);

	/**
	 * Returns sign whether user can changing common server settings
	 * @param [out] buffer for storing result
	 * @return PRL_RESULT. Possible values:
	 * PRL_ERR_SUCCESS - operation completed successfully
	 */
	PRL_RESULT CanChangeSrvSets(PRL_BOOL_PTR pbCanChangeSrvSets);

private:

	UserInfo	m_UserInfo;

};


typedef PrlHandleSmartPtr<PrlHandleUserInfo> PrlHandleUserInfoPtr;


#endif	/* __PARALLELS_HANDLE_USER_INFO_H__ */
